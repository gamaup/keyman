	<li>Untuk menambah jadwal, klik pada mata kuliah pada sidebar sebelah kiri, lalu klik pada kelas yang diinginkan.</li>
	<li>Untuk menghapus jadwal, klik pada mata kuliah di tabel jadwal.</li>
	<li>Untuk menyimpan, klik Saving Mode. Drag list jadwal untuk mengurutkan sesuai keiinginan. Lalu klik Save. Setelah proses menyimpan berhasil, maka kamu bisa Share, View, dan Download jadwal yang telah kamu buat.</li>
	<li>Untuk membuka jadwal yang pernah dibuat, klik Open pada bagian header.</li>
	<li>Gunakan menu Sort List untuk menyortir mata kuliah apa saja yang akan ditampilkan di sidebar sebelah kiri.</li>
	<li><b>Setelah menyimpan jadwal, ada baiknya mencocokkan kembali dengan UNISYS. Karena jadwal di UNISYS dapat berubah sewaktu-waktu.</b></li>
